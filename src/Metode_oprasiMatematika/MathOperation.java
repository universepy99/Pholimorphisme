package Metode_oprasiMatematika;

class MathOperation {
    // Metode untuk menghitung penjumlahan dua bilangan bulat
    public int add(int a, int b) {
        return a + b;
    }

    // Overload metode untuk menghitung penjumlahan tiga bilangan bulat
    public int add(int a, int b, int c) {
        return a + b + c;
    }
}